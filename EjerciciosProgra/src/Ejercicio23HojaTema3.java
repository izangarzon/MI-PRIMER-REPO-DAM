
import java.util.Scanner;

public class Ejercicio23HojaTema3 {

    public static void main(String[] args) {
        for (int i = 1; i <= 10; i += 2) {
            for (int b = 0; b <= 10; b += 2) {
                System.out.println(i + " x " + b + " = " + b * i);
            }
        }
    }

}
